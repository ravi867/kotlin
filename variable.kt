fun main() {
    var myVariable:Int =20
    // var is keyword to declare any new variable to our code.
    // syntax: var variable_name:datatype= variable value
    // we can skip the datatype part to declare the variable.
    // we can change the value of the variable.
    println("the value of my variable is:$myVariable")
    // $ is used to print the value of that variable.
    myVariable =30
    println("now the updated value is :$myVariable")
    val age:Int = 30
    val favno=34
    println("my age is $age and my favorite number is $favno")
}
